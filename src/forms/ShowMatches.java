    private void loadMatches() throws IOException {
        matches = jdbcDAO.getMatches();
        filterMatches();
    }

    private void filterMatches() {
        jButton1.setEnabled(false);
        jButton2.setEnabled(false);
        index = 0;
        String team1 = jComboBox1.getSelectedItem().toString();
        String team2 = jComboBox2.getSelectedItem().toString();
        String team3 = jComboBox4.getSelectedItem().toString();
        filteredMatches = matches;
        if (!team1.equals(" ")) {
            filteredMatches = filteredMatches.stream().filter(match -> match.getTeam1().getName().contains(team1))
                    .collect(Collectors.toList());
        }
        if (!team2.equals(" ")) {
            filteredMatches = filteredMatches.stream().filter(match -> match.getTeam2().getName().contains(team2))
                    .collect(Collectors.toList());
        }
        if (!team3.equals(" ")) {
            filteredMatches = filteredMatches.stream().filter(match -> match.getWinner().getName().contains(team3))
                    .collect(Collectors.toList());
        }
        if (filteredMatches.isEmpty() || filteredMatches.size() == 1) {
            jButton1.setEnabled(false);
            jButton2.setEnabled(false);
        } else if (filteredMatches.size() > 1) {
            jButton1.setEnabled(true);
        }
        if (filteredMatches.size() > 0) {
            setFields();
        } else {
            try {
                emptyFields();
                throw new QuidditchException(QuidditchException.ERROR_NO_MATCHES);
            } catch (QuidditchException ex) {
                System.out.println(ex);
                JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Dialog", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void setFields() {
        // FunciÃ³n que establece los valores para los campos de texto
        this.jTextField1.setText(String.valueOf(filteredMatches.get(index).getIdMatch()));
        this.jTextField2.setText(String.valueOf(filteredMatches.get(index).getDate()));
        this.jTextField3.setText(filteredMatches.get(index).getPlace());
        this.jTextField4.setText(filteredMatches.get(index).getTeam1().getName());
        this.jTextField5.setText(filteredMatches.get(index).getTeam2().getName());
        this.jTextField6.setText(filteredMatches.get(index).getWinner().getName());

    }

    private void emptyFields() {
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText("");
        this.jTextField5.setText("");
        this.jTextField6.setText("");

    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        index++;
        setFields();
        this.jTextField1.setText(String.valueOf(filteredMatches.get(index).getIdMatch()));
        if (index == filteredMatches.size() - 1) {
            jButton1.setEnabled(false);
        }
        if (index > 0) {
            jButton2.setEnabled(true);
        }
    }// GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
        index--;
        setFields();
        if (index == 0) {
            jButton2.setEnabled(false);
        }
        if (index < filteredMatches.size() - 1) {
            jButton1.setEnabled(true);
        }
    }// GEN-LAST:event_jButton2ActionPerformed

