    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        try {
            jdbcDAO.conectar();
            if (jTextField1.getText().equals("") || jTextField2.getText().equals("")
                    || jTextField3.getText().equals("")) {
                throw new QuidditchException(QuidditchException.FILL_ALL_FIELDS);
            }
            if (jdbcDAO.getPlayerByName(jTextField2.getText()) == null) {
                if (!isNumeric(jTextField1.getText())) {
                    throw new QuidditchException(QuidditchException.ERROR_AGE);
                }
                Player p = new Player(jTextField2.getText(), Integer.parseInt(jTextField1.getText()),
                        jComboBox1.getSelectedItem().toString(), jTextField3.getText(),
                        jdbcDAO.getTeamByName(jComboBox2.getSelectedItem().toString()));
                if (jdbcDAO.insertPlayer(p)) {
                    JOptionPane.showMessageDialog(new JFrame(), "Usuario insertado!", "Dialog",
                            JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                } else {
                    throw new QuidditchException(QuidditchException.ERROR_PLAYER);
                }

            } else {
                throw new QuidditchException(QuidditchException.EXIST_PLAYER);
            }
        } catch (QuidditchException ex) {
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Dialog", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);

        }
    }// GEN-LAST:event_jButton1ActionPerformed

    public static boolean isNumeric(String str) {
        try {
            Double.parseDouble(str);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }

