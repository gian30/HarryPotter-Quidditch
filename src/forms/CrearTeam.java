    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        jdbcDAO.conectar();
        try {
            if (jdbcDAO.getTeamByName(jTextField2.getText()) == null) {
                if (jTextField1.getText().equals("") || jTextField2.getText().equals("")) {
                    throw new QuidditchException(QuidditchException.FILL_ALL_FIELDS);
                }
                Team t = new Team(jTextField2.getText(), jTextField1.getText(),
                        convertUtilToSql(jXDatePicker1.getDate()));
                if (jdbcDAO.insertTeam(t)) {
                    JOptionPane.showMessageDialog(new JFrame(), "Equipo creado", "Dialog",
                            JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }
            } else {
                throw new QuidditchException(QuidditchException.ERROR_TEAM);
            }
        } catch (QuidditchException ex) {
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Dialog", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);
        }
    }// GEN-LAST:event_jButton1ActionPerformed

    private static java.sql.Date convertUtilToSql(java.util.Date uDate) {
        try {
            java.sql.Date sDate = new java.sql.Date(uDate.getTime());
            return sDate;
        } catch (Exception ex) {
            try {
                throw new QuidditchException(QuidditchException.WRONG_DATA);
            } catch (QuidditchException ex1) {
                JOptionPane.showMessageDialog(new JFrame(), ex1.getMessage(), "Dialog",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        }
        return null;
    }

