    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jComboBox3ActionPerformed
        Player p = jdbcDAO.getPlayerByName(jComboBox3.getSelectedItem().toString());
        jTextField2.setText(p.getFullName());
        jTextField1.setText(String.valueOf(p.getAge()));
        jTextField3.setText(p.getBroomStick());
        jComboBox1.setSelectedItem(p.getPosition());
        jComboBox2.setSelectedItem(p.getTeam().getName());
    }// GEN-LAST:event_jComboBox3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (isNumeric(jTextField1.getText())) {
                Player p = new Player(jTextField2.getText(), Integer.parseInt(jTextField1.getText()),
                        jComboBox1.getSelectedItem().toString(), jTextField3.getText(),
                        jdbcDAO.getTeamByName(jComboBox2.getSelectedItem().toString()));
                if (jdbcDAO.updatePlayer(p)) {
                    JOptionPane.showMessageDialog(new JFrame(), "Usuario actualizado", "Dialog",
                            JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                } else {
                    throw new QuidditchException(QuidditchException.ERROR_MODIFY_PLAYER);
                }
            } else {
                throw new QuidditchException(QuidditchException.ERROR_AGE);
            }
        } catch (QuidditchException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "Dialog", JOptionPane.ERROR_MESSAGE);

        }
    }// GEN-LAST:event_jButton1ActionPerformed

